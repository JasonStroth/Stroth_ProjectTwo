    //Function compares user input to LinkList of 500 random numbers
    public void searchList(int userValue, Link pos)             // pos = position
           {             
            if(pos.value == userValue)                          //If found, display results
            {
                jTextField1.setText(userValue + " was found!");
            }
            else if (pos.value != userValue)
            {
                searchList(userValue, pos.nextLL);              // <-----run recursive search function again
            }
            else if(pos.value != userValue)
            {
             String n = " The number you entered could not be found. ";
             jTextField1.setText(n);                            // Display results not found;              
            } 
           }
